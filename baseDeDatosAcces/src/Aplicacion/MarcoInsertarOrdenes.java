    private void jButton_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_aceptarActionPerformed
        SQLClass query = new SQLClass();
        query.conectar();
        codigo ="";
        
        //extraemos el c칩digo correcto del jComboBox
        String buscar = jComboBox_empleados.getSelectedItem().toString().substring(0, 9);
            //comprobamos los caracteres objenidos del ComboBox para asegurar el c칩digo correcto
            for(int i=0;i<buscar.length();i++){
                if (Character.isLetter(buscar.charAt(i)) || Character.isDigit(buscar.charAt(i))){
                    codigo += buscar.charAt(i);
                }
            }
        //guardamos los datos en la tabla para pasrlos a la consulta    
        datos[0] = jTextField_codigo.getText();
        datos[1] = jTextField_nombre.getText();    
        datos[2] = codigo;    
        
        query.insertar("Ordenes", datos);
        
        query.cerrarConexion();
        jTextField_codigo.setText("");
        jTextField_nombre.setText("");
        jComboBox_empleados.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_jButton_aceptarActionPerformed

