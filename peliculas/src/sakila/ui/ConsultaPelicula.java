    private void btConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultaActionPerformed
        //recogemos lo que se introduce en el campo de texto
        String cadenaConsulta = jTextField_consulta.getText();
        
        //vemos que rdioButton se ha seleccionado
        if(jRadioButton_titulo.isSelected()){
            
            if (!cadenaConsulta.trim().equals("")) {
                
                ejecutaHQLConsulta(CONSULTA_BASADA_EN_TITULO + cadenaConsulta + "%'");
                
            } else{
                
                JOptionPane.showMessageDialog(null, "Debe introducir un título", "Título", JOptionPane.INFORMATION_MESSAGE);
                
            }  
            
        }else if(jRadioButton_descripcion.isSelected()){
            
            if (!cadenaConsulta.trim().equals("")) {
                
                ejecutaHQLConsulta(CONSULTA_BASADA_EN_DESCRIPCION + cadenaConsulta + "%'");
                
            } else{
                
                JOptionPane.showMessageDialog(null, "Debe introducir la descripción", "Descripción", JOptionPane.INFORMATION_MESSAGE);
                
            }
            
        }else if(jRadioButton_caracteristica.isSelected()){
            
            if (!cadenaConsulta.trim().equals("")) {
                
                ejecutaHQLConsulta(CONSULTA_BASADA_EN_CARACTERISTICAS + cadenaConsulta + "%'");
                
            } else{
                
                JOptionPane.showMessageDialog(null, "Debe introducir las características", "Características", JOptionPane.INFORMATION_MESSAGE);
                
            }
            
        }
        
    }//GEN-LAST:event_btConsultaActionPerformed

