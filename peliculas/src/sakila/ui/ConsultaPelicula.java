    //método que ejecuta la consulta recibida comoparámetro
    private void ejecutaHQLConsulta(String hql) {
        
        try {
            
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query consulta = session.createQuery(hql);
            List resultList = consulta.list();
            mostrarResultados(resultList);
            session.getTransaction().commit();
            
        } catch (HibernateException he) {
            he.printStackTrace();
        }
        
    }

    private void mostrarResultados(List resultList) {
        Vector<Object> oneRow;
        tableDatos = new Vector();
        
        for (Object o : resultList) {
            
            pelicula = (Film) o;
            
            oneRow = new Vector<Object>();
            oneRow.add(pelicula.getFilmId());
            oneRow.add(pelicula.getTitle());
            oneRow.add(pelicula.getDescription());
            oneRow.add(pelicula.getReleaseYear());
            oneRow.add(pelicula.getLanguageByLanguageId());
            oneRow.add(pelicula.getLanguageByOriginalLanguageId());
            oneRow.add(pelicula.getRentalDuration());
            oneRow.add(pelicula.getRentalRate());
            oneRow.add(pelicula.getLength());
            oneRow.add(pelicula.getReplacementCost());
            oneRow.add(pelicula.getRating());
            oneRow.add(pelicula.getSpecialFeatures());
            oneRow.add(pelicula.getLastUpdate());
            
            tableDatos.add(oneRow);
        }
        
        tbListado.setModel(new DefaultTableModel(tableDatos, tableHeaders));
    }
    
